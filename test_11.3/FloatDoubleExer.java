class FloatDoubleExer {
	public static void main(String[] args) {
		double pi = 3.14;
		double r1 = 1.2;
		double r2 = 2.5;
		int r3 = 6;
		double area1 = pi*r1*r1;
		double area2 = pi*r2*r2;
		double area3 = pi*r3*r3;
		System.out.println("圆1的半径：" + r1 +"面积为：" + area1);
		System.out.println("圆2的半径：" + r2 +"面积为：" + area2);
		System.out.println("圆3的半径：" + r3 +"面积为：" + area3);
	}
}
